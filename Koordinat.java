//kelas koordinat untuk menyimpan posisi x dan y 
public class Koordinat {
    private int x;
    private int y;

    public Koordinat(int x, int y) {
        this.x = x;
        this.y = y;
    }

    //getter dan setter 

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}